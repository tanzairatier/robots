function love.conf(t)
    t.title = "Attack of the Robots!"        -- The title of the window the game is in (string)
    t.author = "Joe Krall"        -- The author of the game (string)
    t.console = true           -- Attach a console (boolean, Windows only)
    t.screen.width = 1024       -- The window width (number)
    t.screen.height = 768       -- The window height (number)
end
